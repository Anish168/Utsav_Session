#include <iostream>
using namespace std;
int main()
{
    cout << (5 & 7) << endl;
    cout << (5 & 11) << endl;
    cout << (9 & 8) << endl;

    cout << (5 | 7) << endl;
    cout << (5 | 11) << endl;
    cout << (9 | 8) << endl;

    cout << (39 & 1) << endl;

    cout << (5 ^ 7) << endl;

    return 0;
}